#pragma once

#include "configure.h"
#include "struct.h"


namespace matchn {


/**
* Управляет спрайтами игры.
*/
class ManagerSprite {
public:
    /**
    * Известные менеджеру спрайты.
    * Спрайты сгруппированы по событиям.
    * Каждое событие может иметь один спрайт (образ для визуализации).
    */
    typedef std::string  pathSprite_t;
    typedef std::unordered_map<
        pathSprite_t,
        std::unique_ptr< CL_Sprite >
    > knownSprite_t;


    /**
    * Известные менеджеру фреймы спрайтов.
    */
    typedef std::string  pathFrame_t;
    typedef boost::unordered_map<
        pathFrame_t,
        std::unique_ptr< CL_PixelBuffer >
    >  knownFrame_t;




    ManagerSprite();




public:
    virtual ~ManagerSprite();




    /**
    * @return Спрайт для элемента с учётом события.
    *         nullptr, если спрайт загрузить на удалось.
    *
    * # Фреймы спрайтов сгруппированы по папкам событий - см. допустимые
    *   названия в ManagerEvent::uid< T >().
    * # Фреймы спрайтов должны быть одного размера.
    *
    * @see pathSprite()
    */
    CL_Sprite* sprite(
        CL_GraphicContext&,
        const typelib::size2Int_t&  needVisualSize,
        const kindElement_t&,
        const nameElement_t&,
        const groupEvent_t&
    );




    /**
    * @return Загруженный по указанному пути фрейм в формате ClanLib.
    *         nullptr, если фрейм загрузить на удалось.
    */
    CL_PixelBuffer* frame( const std::string& path );




    /**
    * @return Путь к спрайту элемента с учётом события.
    */
    static inline pathSprite_t pathSprite(
        const kindElement_t&  kind,
        const nameElement_t&  name,
        const groupEvent_t&   ge
    ) {
        const std::string path = PATH_MEDIA + "/" + kind + "/" + name + "/" + ge;
        return path;
    }




private:
    /**
    * Собирает спрайт из фреймов по указанному пути.
    */
    CL_Sprite* loadSprite(
        CL_GraphicContext&,
        const typelib::size2Int_t&  needVisualSize,
        const pathSprite_t&
    );




private:
    knownSprite_t  mKnownSprite;
    knownFrame_t   mKnownFrame;
};


} // matchn
